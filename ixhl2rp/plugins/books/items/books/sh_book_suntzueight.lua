<font color='red' size='4'>Written by Sun Tzu.</font>
<p>
<br>1. Sun Tzu said: In war, the general receives his commands from the sovereign, collects his army and concentrates his forces 
<br>
<br>2. When in difficult country, do not encamp. In country where high roads intersect, join hands with your allies. Do not linger in dangerously isolated positions. In hemmed-in situations, you must resort to stratagem. In desperate position, you must fight. 

<br>
<br>3. There are roads which must not be followed, armies which must be not attacked, towns which must be besieged, positions which must not be contested, commands of the sovereign which must not be obeyed. 

<br>
<br>4. The general who thoroughly understands the advantages that accompany variation of tactics knows how to handle his troops. 

<br>
<br>5. The general who does not understand these, may be well acquainted with the configuration of the country, yet he will not be able to turn his knowledge to practical account. 

<br>
<br>6. So, the student of war who is unversed in the art of war of varying his plans, even though he be acquainted with the Five Advantages, will fail to make the best use of his men. 

<br>
<br>7. Hence in the wise leader's plans, considerations of advantage and of disadvantage will be blended together. 

<br>
<br>8. If our expectation of advantage be tempered in this way, we may succeed in accomplishing the essential part of our schemes. 

<br>
<br>9. If, on the other hand, in the midst of difficulties we are always ready to seize an advantage, we may extricate ourselves from misfortune. 

<br>
<br>10. Reduce the hostile chiefs by inflicting damage on them; and make trouble for them, and keep them constantly engaged; hold out specious allurements, and make them rush to any given point. 

<br>
<br>11. The art of war teaches us to rely not on the likelihood of the enemy's not coming, but on our own readiness to receive him; not on the chance of his not attacking, but rather on the fact that we have made our position unassailable. 

<br>
<br>12. There are five dangerous faults which may affect a general: (1) Recklessness, which leads to destruction; (2) cowardice, which leads to capture; (3) a hasty temper, which can be provoked by insults; (4) a delicacy of honor which is sensitive to shame; (5) over-solicitude for his men, which exposes him to worry and trouble. 

<br>
<br>13. These are the five besetting sins of a general, ruinous to the conduct of war. 

<br>
<br>14. When an army is overthrown and its leader slain, the cause will surely be found among these five dangerous faults. Let them be a subject of meditation. </p>
