<font color='red' size='4'>Anthem for Doomed Youth
<br>Wilfred Owen, 1893 - 1918.</font>

<p>What passing-bells for these who die as cattle? <br>
Only the monstrous anger of the guns. <br>
Only the stuttering rifles’ rapid rattle <br>
Can patter out their hasty orisons. <br>
No mockeries now for them; no prayers nor bells; <br>
Nor any voice of mourning save the choirs, <br>
The shrill, demented choirs of wailing shells; <br>
And bugles calling for them from sad shires. <br>
What candles may be held to speed them all? <br>
Not in the hands of boys, but in their eyes <br>
Shall shine the holy glimmers of good-byes. <br>
The pallor of girls’ brows shall be their pall; <br>
Their flowers the tenderness of patient minds, <br>
And each slow dusk a drawing-down of blinds. <br>
</p>
<font color='red' size='4'>Dulce Et Decorum Est
<br>Wilfred Owen, 1893 - 1918.</font>

<p>Bent double, like old beggars under sacks,<br>
Knock-kneed, coughing like hags, we cursed through sludge,<br>
Till on the haunting flares we turned our backs<br>
And towards our distant rest began to trudge.<br>
Men marched asleep. Many had lost their boots<br>
But limped on, blood-shod. All went lame; all blind;<br>
Drunk with fatigue; deaf even to the hoots<br>
Of tired, outstripped Five-Nines that dropped behind.<br>
<br>
<br>
Gas! Gas! Quick, boys!—An ecstasy of fumbling,<br>
Fitting the clumsy helmets just in time;<br>
But someone still was yelling out and stumbling<br>
And flound’ring like a man in fire or lime...<br>
Dim, through the misty panes and thick green light,<br>
As under a green sea, I saw him drowning.<br>
<br>
<br>
In all my dreams, before my helpless sight,<br>
He plunges at me, guttering, choking, drowning.<br>
<br>
<br>
If in some smothering dreams you too could pace<br>
Behind the wagon that we flung him in,<br>
And watch the white eyes writhing in his face,<br>
His hanging face, like a devil’s sick of sin;<br>
If you could hear, at every jolt, the blood<br>
Come gargling from the froth-corrupted lungs,<br>
Obscene as cancer, bitter as the cud<br>
Of vile, incurable sores on innocent tongues,—<br>
My friend, you would not tell with such high zest<br>
To children ardent for some desperate glory,<br>
The old Lie: Dulce et decorum est<br>
Pro patria mori.<br></p>

<font color='red' size='4'>The Soldier
<br>Rupert Brooke, 1887 – 1915</font>

<p>If I should die, think only this of me:<br>
That there's some corner of a foreign field<br>
That is for ever England. There shall be<br>
In that rich earth a richer dust concealed;<br>
A dust whom England bore, shaped, made aware,<br>
Gave, once, her flowers to love, her ways to roam,<br>
A body of England's, breathing English air,<br>
Washed by the rivers, blest by suns of home.<br>
<br>
And think, this heart, all evil shed away,<br>
A pulse in the eternal mind, no less<br>
Gives somewhere back the thoughts by England given;<br>
Her sights and sounds; dreams happy as her day;<br>
And laughter, learnt of friends; and gentleness,<br>
In hearts at peace, under an English heaven.<br></p>

<font color='red' size='4'>The Charge of the Light Brigade
<br>Alfred Tennyson, 1809 - 1892</font>

<p>Half a league, half a league,  <br>
  Half a league onward,  <br>
All in the valley of Death  <br>
  Rode the six hundred.  <br>
“Forward, the Light Brigade! <br> 
Charge for the guns!” he said:  <br>
Into the valley of Death  <br>
  Rode the six hundred.  <br>
  <br>
“Forward, the Light Brigade!”  <br>
Was there a man dismay’d?    <br>
Not tho’ the soldier knew  <br>
  Some one had blunder’d:  <br>
Theirs not to make reply,  <br>
Theirs not to reason why,  <br>
Theirs but to do and die:     <br>
Into the valley of Death  <br>
  Rode the six hundred.  <br>
  <br>
Cannon to right of them,  <br>
Cannon to left of them,  <br>
Cannon in front of them    <br>
  Volley’d and thunder’d;  <br>
Storm’d at with shot and shell,<br>  
Boldly they rode and well,  <br>
Into the jaws of Death,  <br>
Into the mouth of Hell    <br>
  Rode the six hundred.  <br>
  <br>
Flash’d all their sabres bare,  <br>
Flash’d as they turn’d in air  <br>
Sabring the gunners there,  <br>
Charging an army, while   <br>
  All the world wonder’d:  <br>
Plunged in the battery-smoke  <br>
Right thro’ the line they broke; <br> 
Cossack and Russian  <br>
Reel’d from the sabre-stroke     <br>
  Shatter’d and sunder’d.  <br>
Then they rode back, but not  <br>
  Not the six hundred.  <br>
  <br>
Cannon to right of them,  <br>
Cannon to left of them,      <br>
Cannon behind them  <br>
  Volley’d and thunder’d;  <br>
Storm’d at with shot and shell,  <br>
While horse and hero fell,  <br>
They that had fought so well    <br>
Came thro’ the jaws of Death,  <br>
Back from the mouth of Hell,  <br>
All that was left of them,  <br>
  Left of six hundred.  <br>
  <br>
When can their glory fade?     <br>
O the wild charge they made!  <br>
  All the world wonder’d.  <br>
Honor the charge they made!  <br>
Honor the Light Brigade,  <br>
  Noble six hundred! <br></p>

 <font color='red' size='4'>Come On, Come Back
<br>Stevie Smith, 1902 - 1971</font>

<p>(incident in a future war)<br>
<br>
Left by the ebbing tide of battle<br>
On the field of Austerlitz<br>
The girl soldier Vaudevue sits<br>
Her fingers tap the ground, she is alone<br>
At midnight in the moonlight she is sitting alone on a round flat stone.<br>
<br>
Graded by the Memel Conference first<br>
Of all human exterminators<br>
M L 5<br>
Has left her just alive<br>
Only her memory is dead for evermore.<br>
She fears and cries, Ah me, why am I here?<br>
Sitting alone on a round flat stone on a hummock there.<br>
<br>
Rising, staggering, over the ground she goes<br>
Over the seeming miles of rutted meadow<br>
To the margin of a lake<br>
The sand beneath her feet<br>
Is cold and damp and firm to the waves' beat.<br>
<br>
Quickly - as a child, an idiot, as one without memory -<br>
She strips her uniform off, strips, stands and lunges<br>
Into the icy waters of the adorable lake.<br>
On the surface of the water lies<br>
A ribbon of white moonlight<br>
The waters on either side of the moony track<br>
Are black as her mind,<br>
Her mind is as secret from her<br>
As the water on which she swims,<br>
As secret as profound as ominous.<br>
<br>
Weeping bitterly for her ominous mind, her plight,<br>
Up the river of white moonlight she swims<br>
Until a treacherous undercurrent<br>
Seizing her in an icy amorous embrace<br>
Dives with her, swiftly severing<br>
The waters which close above her head.<br>
<br>
An enemy sentinel<br>
Finding the abandoned clothes<br>
Waits for the swimmer's return<br>
('Come on, come back')<br>
Waiting, whiling away the hour<br>
Whittling a shepherd's pipe from the hollow reeds.<br>
<br>
In the chill light of dawn<br>
Ring out the pipe's wild notes<br>
'Come on, come back.'<br>
<br>
Vaudevue<br>
In the swift and subtle current's close embrace<br>
Sleeps on, stirs not, hears not the familiar tune<br>
Favourite of all the troops of all the armies<br>
Favourite of Vaudevue<br>
For she had sung it too<br>
Marching to Austerlitz,<br>
'Come on, come back'.<br></p>
