<font color='red' size='4'>Written by Sun Tzu.</font>
<p>
1. Sun Tzu said: Whoever is first in the field and awaits the coming of the enemy, will be fresh for the fight; whoever is second in the field and has to hasten to battle will arrive exhausted. 
<br>
<br>2. Therefore the clever combatant imposes his will on the enemy, but does not allow the enemy's will to be imposed on him. 
<br>
<br>3. By holding out advantages to him, he can cause the enemy to approach of his own accord; or, by inflicting damage, he can make it impossible for the enemy to draw near. 
<br>
<br>4. If the enemy is taking his ease, he can harass him; if well supplied with food, he can starve him out; if quietly encamped, he can force him to move. 
<br>
<br>5. Appear at points which the enemy must hasten to defend; march swiftly to places where you are not expected. 
<br>
<br>6. An army may march great distances without distress, if it marches through country where the enemy is not. 
<br>
<br>7. You can be sure of succeeding in your attacks if you only attack places which are undefended.You can ensure the safety of your defense if you only hold positions that cannot be attacked. 
<br>
<br>8. Hence that general is skillful in attack whose opponent does not know what to defend; and he is skillful in defense whose opponent does not know what to attack. 
<br>
<br>9. O divine art of subtlety and secrecy! Through you we learn to be invisible, through you inaudible; and hence we can hold the enemy's fate in our hands. 
<br>
<br>10. You may advance and be absolutely irresistible, if you make for the enemy's weak points; you may retire and be safe from pursuit if your movements are more rapid than those of the enemy. 
<br>
<br>11. If we wish to fight, the enemy can be forced to an engagement even though he be sheltered behind a high rampart and a deep ditch. All we need do is attack some other place that he will be obliged to relieve. 
<br>
<br>12. If we do not wish to fight, we can prevent the enemy from engaging us even though the lines of our encampment be merely traced out on the ground. All we need do is to throw something odd and unaccountable in his way. 
<br>
<br>13. By discovering the enemy's dispositions and remaining invisible ourselves, we can keep our forces concentrated, while the enemy's must be divided. 
<br>
<br>14. We can form a single united body, while the enemy must split up into fractions. Hence there will be a whole pitted against separate parts of a whole, which means that we shall be many to the enemy's few. 
<br>
<br>15. And if we are able thus to attack an inferior force with a superior one, our opponents will be in dire straits. 
<br>
<br>16. The spot where we intend to fight must not be made known; for then the enemy will have to prepare against a possible attack at several different points; and his forces being thus distributed in many directions, the numbers we shall have to face at any given point will be proportionately few. 
<br>
<br>17. For should the enemy strengthen his van, he will weaken his rear; should he strengthen his rear, he will weaken his van; should he strengthen his left, he will weaken his right; should he strengthen his right, he will weaken his left. If he sends reinforcements everywhere, he will everywhere be weak. 
<br>
<br>18. Numerical weakness comes from having to prepare against possible attacks; numerical strength, from compelling our adversary to make these preparations against us. 
<br>
<br>19. Knowing the place and the time of the coming battle, we may concentrate from the greatest distances in order to fight. 
<br>
<br>20. But if neither time nor place be known, then the left wing will be impotent to succor the right, the right equally impotent to succor the left, the van unable to relieve the rear, or the rear to support the van. How much more so if the furthest portions of the army are anything under a hundred LI apart, and even the nearest are separated by several LI! 
<br>
<br>21. Though according to my estimate the soldiers of Yueh exceed our own in number, that shall advantage them nothing in the matter of victory. I say then that victory can be achieved. 
<br>
<br>22. Though the enemy be stronger in numbers, we may prevent him from fighting. Scheme so as to discover his plans and the likelihood of their success. 
<br>
<br>23. Rouse him, and learn the principle of his activity or inactivity. Force him to reveal himself, so as to find out his vulnerable spots. 
<br>
<br>24. Carefully compare the opposing army with your own, so that you may know where strength is superabundant and where it is deficient. 
<br>
<br>25. In making tactical dispositions, the highest pitch you can attain is to conceal them; conceal your dispositions, and you will be safe from the prying of the subtlest spies, from the machinations of the wisest brains. 
<br>
<br>26. How victory may be produced for them out of the enemy's own tactics--that is what the multitude cannot comprehend. 
<br>
<br>27. All men can see the tactics whereby I conquer, but what none can see is the strategy out of which victory is evolved. 
<br>
<br>28. Do not repeat the tactics which have gained you one victory, but let your methods be regulated by the infinite variety of circumstances. 
<br>
<br>29. Military tactics are like unto water; for water in its natural course runs away from high places and hastens downwards. 
<br>
<br>30. So in war, the way is to avoid what is strong and to strike at what is weak. 
<br>
<br>31. Water shapes its course according to the nature of the ground over which it flows; the soldier works out his victory in relation to the foe whom he is facing. 
<br>
<br>32. Therefore, just as water retains no constant shape, so in warfare there are no constant conditions. 
<br>
<br>33. He who can modify his tactics in relation to his opponent and thereby succeed in winning, may be called a heaven-born captain. 
<br>
<br>34. The five elements (water, fire, wood, metal, earth) are not always equally predominant; the four seasons make way for each other in turn. There are short days and long; the moon has its periods of waning and waxing. </p>
