<font color='red' size='4'>Written by Sun Tzu.</font>

<p>1. Sun Tzu said: In war, the general receives his commands from the sovereign. 
<br>
<br>2. Having collected an army and concentrated his forces, he must blend and harmonize the different elements thereof before pitching his camp. 
<br>
<br>3. After that, comes tactical maneuvering, than which there is nothing more difficult. The difficulty of tactical maneuvering consists in turning the devious into the direct, and misfortune into gain.
<br>
<br>4. Thus, to take a long and circuitous route, after enticing the enemy out of the way, and though starting after him, to contrive to reach the goal before him, shows knowledge of the artifice of deviation. 
<br>
<br>5. Maneuvering with an army is advantageous; with an undisciplined multitude, most dangerous. 
<br>
<br>6. If you set a fully equipped army in march in order to snatch an advantage, the chances are that you will be too late. On the other hand, to detach a flying column for the purpose involves the sacrifice of its baggage and stores. 
<br>
<br>7. Thus, if you order your men to roll up their buff-coats, and make forced marches without halting day or night, covering double the usual distance at a stretch, doing a hundred LI in order to wrest an advantage, the leaders of all your three divisions will fall into the hands of the enemy. 
<br>
<br>8. The stronger men will be in front, the jaded ones will fall behind, and on this plan only one-tenth of your army will reach its destination. 
<br>
<br>9. If you march fifty LI in order to outmaneuver the enemy, you will lose the leader of your first division, and only half your force will reach the goal. 
<br>
<br>10. If you march thirty LI with the same object, two-thirds of your army will arrive. 
<br>
<br>11. We may take it then that an army without its baggage-train is lost; without provisions it is lost; without bases of supply it is lost. 
<br>
<br>12. We cannot enter into alliances until we are acquainted with the designs of our neighbors. 
<br>
<br>13. We are not fit to lead an army on the march unless we are familiar with the face of the country--its mountains and forests, its pitfalls and precipices, its marshes and swamps. 
<br>
<br>14. We shall be unable to turn natural advantage to account unless we make use of local guides. 
<br>
<br>15. In war, practice dissimulation, and you will succeed. 
<br>
<br>16. Whether to concentrate or to divide your troops, must be decided by circumstances. 
<br>
<br>17. Let your rapidity be that of the wind, your compactness that of the forest. 
<br>
<br>18. In raiding and plundering be like fire, is immovability like a mountain. 
<br>
<br>19. Let your plans be dark and impenetrable as night, and when you move, fall like a thunderbolt. 
<br>
<br>20. When you plunder a countryside, let the spoil be divided amongst your men; when you capture new territory, cut it up into allotments for the benefit of the soldiery. 
<br>
<br>21. Ponder and deliberate before you make a move. 
<br>
<br>22. He will conquer who has learnt the artifice of deviation. Such is the art of maneuvering. 
<br>
<br>23. The Book of Army Management says: On the field of battle, the spoken word does not carry far enough: hence the institution of gongs and drums. Nor can ordinary objects be seen clearly enough: hence the institution of banners and flags. 
<br>
<br>24. Gongs and drums, banners and flags, are means whereby the ears and eyes of the host may be focused on one particular point. 
<br>
<br>25. The host thus forming a single united body, is it impossible either for the brave to advance alone, or for the cowardly to retreat alone. This is the art of handling large masses of men. 
<br>
<br>26. In night-fighting, then, make much use of signal-fires and drums, and in fighting by day, of flags and banners, as a means of influencing the ears and eyes of your army. 
<br>
<br>27. A whole army may be robbed of its spirit; a commander-in-chief may be robbed of his presence of mind. 
<br>
<br>28. Now a soldier's spirit is keenest in the morning; by noonday it has begun to flag; and in the evening, his mind is bent only on returning to camp. 
<br>
<br>29. A clever general, therefore, avoids an army when its spirit is keen, but attacks it when it is sluggish and inclined to return. This is the art of studying moods. 
<br>
<br>30. Disciplined and calm, to await the appearance of disorder and hubbub amongst the enemy:--this is the art of retaining self-possession. 
<br>
<br>31. To be near the goal while the enemy is still far from it, to wait at ease while the enemy is toiling and struggling, to be well-fed while the enemy is famished:--this is the art of husbanding one's strength. 
<br>
<br>32. To refrain from intercepting an enemy whose banners are in perfect order, to refrain from attacking an army drawn up in calm and confident array:--this is the art of studying circumstances. 
<br>
<br>33. It is a military axiom not to advance uphill against the enemy, nor to oppose him when he comes downhill. 
<br>
<br>34. Do not pursue an enemy who simulates flight; do not attack soldiers whose temper is keen. 
<br>
<br>35. Do not swallow bait offered by the enemy. Do not interfere with an army that is returning home. 
<br>
<br>36. When you surround an army, leave an outlet free. Do not press a desperate foe too hard. 
<br>
<br>37. Such is the art of warfare. </p>
