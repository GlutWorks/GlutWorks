<font color='red' size='4'>Written by Sun Tzu.</font>
<p>Sun Tzu said: In the operations of war, where there are in the field a thousand swift chariots, as many heavy chariots, and a hundred thousand mail-clad soldiers, with provisions enough to carry them a thousand li, the expenditure at home and at the front, including entertainment of guests, small items such as glue and paint, and sums spent on chariots and armor, will reach the total of a thousand ounces of silver per day. Such is the cost of raising an army of 100,000 men. </p>

<p>2. When you engage in actual fighting, if victory is long in coming, then men's weapons will grow dull and their ardor will be damped. If you lay siege to a town, you will exhaust your strength. </p>

<p>3. Again, if the campaign is protracted, the resources of the State will not be equal to the strain. </p>

<p>4. Now, when your weapons are dulled, your ardor damped, your strength exhausted and your treasure spent, other chieftains will spring up to take advantage of your extremity. Then no man, however wise, will be able to avert the consequences that must ensue. </p>

<p>5. Thus, though we have heard of stupid haste in war, cleverness has never been seen associated with long delays. </p>

<p>6. There is no instance of a country having benefited from prolonged warfare. </p>

<p>7. It is only one who is thoroughly acquainted with the evils of war that can thoroughly understand the profitable way of carrying it on. </p>

<p>8. The skillful soldier does not raise a second levy, neither are his supply-wagons loaded more than twice. </p>

<p>9. Bring war material with you from home, but forage on the enemy. Thus the army will have food enough for its needs. </p>

<p>10. Poverty of the State exchequer causes an army to be maintained by contributions from a distance. Contributing to maintain an army at a distance causes the people to be impoverished. </p>

<p>11. On the other hand, the proximity of an army causes prices to go up; and high prices cause the people's substance to be drained away. </p>

<p>12. When their substance is drained away, the peasantry will be afflicted by heavy exactions. </p>

<p>13,14. With this loss of substance and exhaustion of strength, the homes of the people will be stripped bare, and three-tenths of their income will be dissipated; while government expenses for broken chariots, worn-out horses, breast-plates and helmets, bows and arrows, spears and shields, protective mantles, draught-oxen and heavy wagons, will amount to four-tenths of its total revenue. </p>

<p>15. Hence a wise general makes a point of foraging on the enemy. One cartload of the enemy's provisions is equivalent to twenty of one's own, and likewise a single picul of his provender is equivalent to twenty from one's own store. </p>

<p>16. Now in order to kill the enemy, our men must be roused to anger; that there may be advantage from defeating the enemy, they must have their rewards. </p>

<p>17. Therefore in chariot fighting, when ten or more chariots have been taken, those should be rewarded who took the first. Our own flags should be substituted for those of the enemy, and the chariots mingled and used in conjunction with ours. The captured soldiers should be kindly treated and kept. </p>

<p>18. This is called, using the conquered foe to augment one's own strength. </p>

<p>19. In war, then, let your great object be victory, not lengthy campaigns. </p>

<p>20. Thus it may be known that the leader of armies is the arbiter of the people's fate, the man on whom it depends whether the nation shall be in peace or in peril. </p>
