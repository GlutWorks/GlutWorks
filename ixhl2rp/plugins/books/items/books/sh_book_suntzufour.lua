<font color='red' size='4'>Written by Sun Tzu.</font>
<p>
<br>1. Sun Tzu said: The good fighters of old first put themselves beyond the possibility of defeat, and then waited for an opportunity of defeating the enemy. 
<br>
<br>2. To secure ourselves against defeat lies in our own hands, but the opportunity of defeating the enemy is provided by the enemy himself. 
<br>
<br>3. Thus the good fighter is able to secure himself against defeat, but cannot make certain of defeating the enemy. 
<br>
<br>4. Hence the saying: One may know how to conquer without being able to do it. 
<br>
<br>5. Security against defeat implies defensive tactics; ability to defeat the enemy means taking the offensive. 
<br>
<br>6. Standing on the defensive indicates insufficient strength; attacking, a superabundance of strength. 
<br>
<br>7. The general who is skilled in defense hides in the most secret recesses of the earth; he who is skilled in attack flashes forth from the topmost heights of heaven. Thus on the one hand we have ability to protect ourselves; on the other, a victory that is complete. 
<br>
<br>8. To see victory only when it is within the ken of the common herd is not the acme of excellence. 
<br>
<br>9. Neither is it the acme of excellence if you fight and conquer and the whole Empire says, "Well done!" 
<br>
<br>10. To lift an autumn hair is no sign of great strength; to see the sun and moon is no sign of sharp sight; to hear the noise of thunder is no sign of a quick ear. 
<br>
<br>11. What the ancients called a clever fighter is one who not only wins, but excels in winning with ease. 
<br>
<br>12. Hence his victories bring him neither reputation for wisdom nor credit for courage. 
<br>
<br>13. He wins his battles by making no mistakes. Making no mistakes is what establishes the certainty of victory, for it means conquering an enemy that is already defeated. 
<br>
<br>14. Hence the skillful fighter puts himself into a position which makes defeat impossible, and does not miss the moment for defeating the enemy. 
<br>
<br>15. Thus it is that in war the victorious strategist only seeks battle after the victory has been won, whereas he who is destined to defeat first fights and afterwards looks for victory. 
<br>
<br>16. The consummate leader cultivates the moral law, and strictly adheres to method and discipline; thus it is in his power to control success. 
<br>
<br>17. In respect of military method, we have, firstly, Measurement; secondly, Estimation of quantity; thirdly, Calculation; fourthly, Balancing of chances; fifthly, Victory. 
<br>
<br>18. Measurement owes its existence to Earth; Estimation of quantity to Measurement; Calculation to Estimation of quantity; Balancing of chances to Calculation; and Victory to Balancing of chances. 
<br>
<br>19. A victorious army opposed to a routed one, is as a pound's weight placed in the scale against a single grain. 
<br>
<br>20. The onrush of a conquering force is like the bursting of pent-up waters into a chasm a thousand fathoms deep. </p>
