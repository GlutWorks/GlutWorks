<font color='red' size='4'>Written by Sun Tzu.</font>
<p>1. Sun Tzu said: There are five ways of attacking with fire. The first is to burn soldiers in their camp; the second is to burn stores; the third is to burn baggage trains; the fourth is to burn arsenals and magazines; the fifth is to hurl dropping fire amongst the enemy.</p>
<p>2. In order to carry out an attack, we must have means available. The material for raising fire should always be kept in readiness.</p>
<p>3. There is a proper season for making attacks with fire, and special days for starting a conflagration.</p>
<p>4. The proper season is when the weather is very dry; the special days are those when the moon is in the constellations of the Sieve, the Wall, the Wing or the Cross-bar; for these four are all days of rising wind.</p>
<p>5. In attacking with fire, one should be prepared to meet five possible developments:</p>
<p>6. When fire breaks out inside to enemy's camp, respond at once with an attack from without.
<br>If there is an outbreak of fire, but the enemy's soldiers remain quiet, bide your time and do not attack.
<br>When the force of the flames has reached its height, follow it up with an attack, if that is practicable; if not, stay where you are.
<br>If it is possible to make an assault with fire from without, do not wait for it to break out within, but deliver your attack at a favorable moment.
<br>When you start a fire, be to windward of it. Do not attack from the leeward.</p>
<p>A wind that rises in the daytime lasts long, but a night breeze soon falls.</p>

<p>In every army, the five developments connected with fire must be known, the movements of the stars calculated, and a watch kept for the proper days.</p>

<p>Hence those who use fire as an aid to the attack show intelligence; those who use water as an aid to the attack gain an accession of strength.</p>

<p>By means of water, an enemy may be intercepted, but not robbed of all his belongings.</p>

<p>Unhappy is the fate of one who tries to win his battles and succeed in his attacks without cultivating the spirit of enterprise; for the result is waste of time and general stagnation.</p>

<p>Hence the saying: The enlightened ruler lays his plans well ahead; the good general cultivates his resources.</p>

<p>Move not unless you see an advantage; use not your troops unless there is something to be gained; fight not unless the position is critical.</p>

<p>No ruler should put troops into the field merely to gratify his own spleen; no general should fight a battle simply out of pique.</p>

<p>If it is to your advantage, make a forward move; if not, stay where you are.</p>

<p>Anger may in time change to gladness; vexation may be succeeded by content.</p>

<p>But a kingdom that has once been destroyed can never come again into being; nor can the dead ever be brought back to life.</p>

<p>Hence the enlightened ruler is heedful, and the good general full of caution. This is the way to keep a country at peace and an army intact.</p>
