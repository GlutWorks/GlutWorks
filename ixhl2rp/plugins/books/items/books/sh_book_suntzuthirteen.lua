<font color='red' size='4'>Written by Sun Tzu.</font>
<p>1. Sun Tzu said: Raising a host of a hundred thousand men and marching them great distances entails heavy loss on the people and a drain on the resources of the State. The daily expenditure will amount to a thousand ounces of silver. There will be commotion at home and abroad, and men will drop down exhausted on the highways. As many as seven hundred thousand families will be impeded in their labor. 
<br>
<br>2. Hostile armies may face each other for years, striving for the victory which is decided in a single day. This being so, to remain in ignorance of the enemy's condition simply because one grudges the outlay of a hundred ounces of silver in honors and emoluments, is the height of inhumanity. 

<br>
<br>3. One who acts thus is no leader of men, no present help to his sovereign, no master of victory. 

<br>
<br>4. Thus, what enables the wise sovereign and the good general to strike and conquer, and achieve things beyond the reach of ordinary men, is foreknowledge. 

<br>
<br>5. Now this foreknowledge cannot be elicited from spirits; it cannot be obtained inductively from experience, nor by any deductive calculation. 

<br>
<br>6. Knowledge of the enemy's dispositions can only be obtained from other men. 

<br>
<br>7. Hence the use of spies, of whom there are five classes: (1) Local spies; (2) inward spies; (3) converted spies; (4) doomed spies; (5) surviving spies. 

<br>
<br>8. When these five kinds of spy are all at work, none can discover the secret system. This is called "divine manipulation of the threads." It is the sovereign's most precious faculty. 

<br>
<br>9. Having local spies means employing the services of the inhabitants of a district. 

<br>
<br>10. Having inward spies, making use of officials of the enemy. 

<br>
<br>11. Having converted spies, getting hold of the enemy's spies and using them for our own purposes. 

<br>
<br>12. Having doomed spies, doing certain things openly for purposes of deception, and allowing our spies to know of them and report them to the enemy. 

<br>
<br>13. Surviving spies, finally, are those who bring back news from the enemy's camp. 

<br>
<br>14. Hence it is that which none in the whole army are more intimate relations to be maintained than with spies. None should be more liberally rewarded. In no other business should greater secrecy be preserved. 

<br>
<br>15. Spies cannot be usefully employed without a certain intuitive sagacity. 

<br>
<br>16. They cannot be properly managed without benevolence and straightforwardness. 

<br>
<br>17. Without subtle ingenuity of mind, one cannot make certain of the truth of their reports. 

<br>
<br>18. Be subtle! be subtle! and use your spies for every kind of business. 

<br>
<br>19. If a secret piece of news is divulged by a spy before the time is ripe, he must be put to death together with the man to whom the secret was told. 

<br>
<br>20. Whether the object be to crush an army, to storm a city, or to assassinate an individual, it is always necessary to begin by finding out the names of the attendants, the aides-de-camp, and door-keepers and sentries of the general in command. Our spies must be commissioned to ascertain these. 

<br>
<br>21. The enemy's spies who have come to spy on us must be sought out, tempted with bribes, led away and comfortably housed. Thus they will become converted spies and available for our service. 

<br>
<br>22. It is through the information brought by the converted spy that we are able to acquire and employ local and inward spies. 

<br>
<br>23. It is owing to his information, again, that we can cause the doomed spy to carry false tidings to the enemy. 

<br>
<br>24. Lastly, it is by his information that the surviving spy can be used on appointed occasions. 

<br>
<br>25. The end and aim of spying in all its five varieties is knowledge of the enemy; and this knowledge can only be derived, in the first instance, from the converted spy. Hence it is essential that the converted spy be treated with the utmost liberality. 

<br>
<br>26. Of old, the rise of the Yin dynasty was due to I Chih who had served under the Hsia. Likewise, the rise of the Chou dynasty was due to Lu Ya who had served under the Yin. 

<br>
<br>27. Hence it is only the enlightened ruler and the wise general who will use the highest intelligence of the army for purposes of spying and thereby they achieve great results. Spies are a most important element in water, because on them depends an army's ability to move. </p>
